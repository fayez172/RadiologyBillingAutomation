  sectionTitle: { fontSize: 12, fontWeight: 'bold', marginBottom: 5 },
  table: { width: '100%', marginBottom: 30 },
  tableHeader: { flexDirection: 'row', borderBottomWidth: 1, borderBottomColor: '#000', paddingBottom: 5, marginBottom: 5, fontWeight: 'bold' },
  tableRow: { flexDirection: 'row', marginBottom: 5 },
  col1: { width: '40%' },
  col2: { width: '20%', textAlign: 'right' },
  col3: { width: '20%', textAlign: 'right' },
  col4: { width: '20%', textAlign: 'right' },
  summary: { marginTop: 20, borderTopWidth: 1, borderTopColor: '#000', paddingTop: 10, alignItems: 'flex-end' },
  summaryRow: { flexDirection: 'row', marginBottom: 5, width: 200, justifyContent: 'space-between' },
  summaryLabel: { fontWeight: 'bold' },
  totalRow: { flexDirection: 'row', marginTop: 5, paddingTop: 5, borderTopWidth: 2, borderTopColor: '#000', width: 200, justifyContent: 'space-between' },
