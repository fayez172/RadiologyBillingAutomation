const InvoicePDF = ({ invoice }: { invoice: any }) => {
  const formatCur = (num: number) => `BDT ${Number(num).toLocaleString()}`;
  const formatDate = (date: Date) => new Date(date).toLocaleDateString('en-GB', { day: '2-digit', month: 'short', year: 'numeric' });

  return (
    <Document>
      <Page size="A4" style={styles.page}>
        <View style={styles.header}>
          <View>
            <Text style={styles.title}>INVOICE</Text>
            <Text style={{ marginTop: 5 }}>Care360 TeleRadiology Billing</Text>
            <Text>123 Medical Hub, Dhaka</Text>
            <Text>contact@rsb.com.bd</Text>
          </View>
          <View style={styles.invoiceInfo}>
            <Text style={{ fontWeight: 'bold' }}>Invoice #: {invoice.invoice_number}</Text>
            <Text>Date: {formatDate(invoice.finalized_at || invoice.created_at)}</Text>
            <Text>Period: {formatDate(invoice.period_start)} - {formatDate(invoice.period_end)}</Text>
          </View>
        </View>

        <View style={styles.clientInfo}>
          <Text style={styles.sectionTitle}>Billed To:</Text>
          <Text>{invoice.client.name}</Text>
          <Text>{invoice.client.contact_email || 'No email provided'}</Text>
        </View>

        <View style={styles.table}>
          <View style={styles.tableHeader}>
            <Text style={styles.col1}>Study Type</Text>
            <Text style={styles.col2}>Quantity</Text>
            <Text style={styles.col3}>Unit Price</Text>
            <Text style={styles.col4}>Total</Text>
          </View>
          {invoice.lines.map((line: any, idx: number) => (
            <View key={idx} style={styles.tableRow}>
              <Text style={styles.col1}>{line.type}</Text>
              <Text style={styles.col2}>{line.qty}</Text>
              <Text style={styles.col3}>{formatCur(line.unit_price)}</Text>
              <Text style={styles.col4}>{formatCur(line.total)}</Text>
            </View>
          ))}
        </View>

        <View style={styles.summary}>
          <View style={styles.summaryRow}>
            <Text style={styles.summaryLabel}>Subtotal:</Text>
            <Text>{formatCur(invoice.subtotal)}</Text>
          </View>
          <View style={styles.summaryRow}>
            <Text style={styles.summaryLabel}>Previous Due:</Text>
            <Text>{formatCur(invoice.previous_due)}</Text>
          </View>
          <View style={styles.totalRow}>
            <Text style={styles.totalLabel}>Total Due:</Text>
            <Text style={styles.totalValue}>{formatCur(invoice.total_due)}</Text>
          </View>
        </View>

        <View style={styles.footer}>
          <Text>Please make checks payable to "Care360 TeleRadiology".</Text>
          <Text>Generated by TeleRadiology Billing Automation Platform</Text>
        </View>
      </Page>
    </Document>
  );
};

export async function GET(req: Request, { params }: { params: { id: string } }) {
  try {
    const session = await getServerSession(authOptions);
    if (!session?.user) return apiError('UNAUTHORIZED', 'Unauthorized', 401);

    const invoice = await prisma.invoice.findUnique({
      where: { id: params.id },
      include: {
        client: true,
        lines: { orderBy: { type: 'asc' } }
      }
    });

    if (!invoice) return apiError('NOT_FOUND', 'Invoice not found', 404);

    const stream = await renderToStream(<InvoicePDF invoice={invoice} />);
    
    // Return standard stream Response
    return new Response(stream as any, {
      headers: {
        'Content-Type': 'application/pdf',
        'Content-Disposition': `inline; filename="Invoice_${invoice.invoice_number}.pdf"`
      }
    });
  } catch (err: any) {
    console.error('[INVOICE_PDF_GET]', err);
    return apiError('INTERNAL_ERROR', 'Failed to generate PDF', 500);
  }
}
